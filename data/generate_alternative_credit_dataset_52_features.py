MISS_RATES = {
    "very_poor": 0.18,
    "poor":      0.12,
    "fair":      0.08,
    "good":      0.05,
    "very_good": 0.03,
    "excellent": 0.02,
}

all_rows = []
for seg_name, count, score_lo, score_hi in segments:
    profile = FEATURE_PROFILES[seg_name]
    miss    = MISS_RATES[seg_name]
    for _ in range(count):
        row = {}
        for feat, (lo, hi) in profile.items():
            val = np.random.uniform(lo, hi)
            if np.random.random() < miss:
                val = np.nan
            row[feat] = val
        all_rows.append(row)

df_out = pd.DataFrame(all_rows)
df_out = df_out.sample(frac=1, random_state=42).reset_index(drop=True)
